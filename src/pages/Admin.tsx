import { useState, useEffect } from "react";
import { useNavigate } from "react-router-dom";
import { supabase } from "@/lib/supabase";
import { Button } from "@/components/ui/button";
import { Card } from "@/components/ui/card";
import { Avatar, AvatarFallback, AvatarImage } from "@/components/ui/avatar";
import { Input } from "@/components/ui/input";
import { Tabs, TabsContent, TabsList, TabsTrigger } from "@/components/ui/tabs";
import { Badge } from "@/components/ui/badge";
import { 
  ArrowLeft, 
  Users, 
  FileWarning, 
  Shield, 
  Ban, 
  CheckCircle2, 
  Search,
  Trash2,
  Eye,
  AlertTriangle,
  TrendingUp,
  Settings,
  BarChart3,
  UserCheck
} from "lucide-react";
import ProtectedRoute from "@/components/ProtectedRoute";
import VerificationBadge from "@/components/VerificationBadge";
import { toast } from "sonner";
import { motion } from "framer-motion";
import {
  AlertDialog,
  AlertDialogAction,
  AlertDialogCancel,
  AlertDialogContent,
  AlertDialogDescription,
  AlertDialogFooter,
  AlertDialogHeader,
  AlertDialogTitle,
} from "@/components/ui/alert-dialog";

interface User {
  id: string;
  username: string;
  full_name: string;
  first_name: string;
  avatar_url: string;
  verified: boolean;
  badge_type: string | null;
  created_at: string;
}

interface Report {
  id: string;
  reporter_id: string;
  reported_content_id: string;
  content_type: string;
  reason: string;
  status: string;
  created_at: string;
  reporter?: {
    username: string;
    avatar_url: string;
  };
}

export default function Admin() {
  const navigate = useNavigate();
  const [isAdmin, setIsAdmin] = useState(false);
  const [loading, setLoading] = useState(true);
  const [users, setUsers] = useState<User[]>([]);
  const [reports, setReports] = useState<Report[]>([]);
  const [searchQuery, setSearchQuery] = useState("");
  const [deleteDialog, setDeleteDialog] = useState<{ open: boolean; userId: string; username: string }>({ 
    open: false, 
    userId: "", 
    username: "" 
  });
  const [stats, setStats] = useState({
    totalUsers: 0,
    verifiedUsers: 0,
    pendingReports: 0,
    blockedAccounts: 0
  });

  useEffect(() => {
    checkAdminStatus();
  }, []);

  const checkAdminStatus = async () => {
    const { data: { user } } = await supabase.auth.getUser();
    if (!user) {
      navigate("/auth");
      return;
    }

    const { data: roleData } = await supabase
      .from("user_roles")
      .select("role")
      .eq("user_id", user.id)
      .eq("role", "admin")
      .maybeSingle();

    if (!roleData) {
      toast.error("Acesso negado");
      navigate("/feed");
      return;
    }

    setIsAdmin(true);
    await loadData();
    setLoading(false);
  };

  const loadData = async () => {
    await Promise.all([loadUsers(), loadReports(), loadStats()]);
  };

  const loadUsers = async () => {
    const { data } = await supabase
      .from("profiles")
      .select("*")
      .order("created_at", { ascending: false })
      .limit(100);
    
    setUsers(data || []);
  };

  const loadReports = async () => {
    const { data } = await supabase
      .from("reports")
      .select("*")
      .eq("status", "pending")
      .order("created_at", { ascending: false });
    
    setReports(data || []);
  };

  const loadStats = async () => {
    const [usersCount, verifiedCount, reportsCount, blockedCount] = await Promise.all([
      supabase.from("profiles").select("*", { count: "exact", head: true }),
      supabase.from("profiles").select("*", { count: "exact", head: true }).eq("verified", true),
      supabase.from("reports").select("*", { count: "exact", head: true }).eq("status", "pending"),
      supabase.from("blocked_accounts").select("*", { count: "exact", head: true })
    ]);

    setStats({
      totalUsers: usersCount.count || 0,
      verifiedUsers: verifiedCount.count || 0,
      pendingReports: reportsCount.count || 0,
      blockedAccounts: blockedCount.count || 0
    });
  };

  const handleVerifyUser = async (userId: string, verify: boolean) => {
    const { error } = await supabase
      .from("profiles")
      .update({ verified: verify, badge_type: verify ? "blue" : null })
      .eq("id", userId);

    if (error) {
      toast.error("Erro ao atualizar verificação");
      return;
    }

    toast.success(verify ? "Usuário verificado!" : "Verificação removida");
    loadUsers();
    loadStats();
  };

  const handleBlockUser = async (userId: string) => {
    const { data: { user } } = await supabase.auth.getUser();
    if (!user) return;

    const { data: profileData } = await supabase
      .from("profiles")
      .select("email")
      .eq("id", userId)
      .single();

    if (profileData?.email === "isaacmuaco582@gmail.com") {
      toast.error("Esta conta não pode ser bloqueada");
      return;
    }

    const { error } = await supabase
      .from("blocked_accounts")
      .insert({ user_id: userId, blocked_by: user.id, reason: "Bloqueado pelo admin" });

    if (error) {
      toast.error("Erro ao bloquear usuário");
      return;
    }

    toast.success("Usuário bloqueado");
    loadStats();
  };

  const handleDeleteUser = async (userId: string) => {
    const { data: profileData } = await supabase
      .from("profiles")
      .select("email")
      .eq("id", userId)
      .single();

    if (profileData?.email === "isaacmuaco582@gmail.com") {
      toast.error("Esta conta não pode ser excluída");
      setDeleteDialog({ open: false, userId: "", username: "" });
      return;
    }

    await supabase.from("posts").delete().eq("user_id", userId);
    await supabase.from("stories").delete().eq("user_id", userId);
    await supabase.from("verification_videos").delete().eq("user_id", userId);
    
    const { error } = await supabase.from("profiles").delete().eq("id", userId);

    if (error) {
      toast.error("Erro ao excluir usuário");
      return;
    }

    toast.success("Usuário excluído");
    setDeleteDialog({ open: false, userId: "", username: "" });
    loadUsers();
    loadStats();
  };

  const handleResolveReport = async (reportId: string, action: "resolved" | "dismissed") => {
    const { data: { user } } = await supabase.auth.getUser();
    if (!user) return;

    const { error } = await supabase
      .from("reports")
      .update({ 
        status: action, 
        resolved_by: user.id, 
        resolved_at: new Date().toISOString() 
      })
      .eq("id", reportId);

    if (error) {
      toast.error("Erro ao resolver denúncia");
      return;
    }

    toast.success(action === "resolved" ? "Denúncia resolvida" : "Denúncia ignorada");
    loadReports();
    loadStats();
  };

  const filteredUsers = users.filter(user => 
    user.username?.toLowerCase().includes(searchQuery.toLowerCase()) ||
    user.full_name?.toLowerCase().includes(searchQuery.toLowerCase())
  );

  if (loading) {
    return (
      <ProtectedRoute>
        <div className="min-h-screen bg-background flex items-center justify-center">
          <div className="animate-spin rounded-full h-8 w-8 border-2 border-primary border-t-transparent" />
        </div>
      </ProtectedRoute>
    );
  }

  if (!isAdmin) return null;

  return (
    <ProtectedRoute>
      <div className="min-h-screen bg-background">
        {/* Modern Header */}
        <div className="sticky top-0 z-50 bg-gradient-to-r from-primary via-primary/90 to-accent text-white">
          <div className="flex items-center justify-between px-4 h-16">
            <div className="flex items-center gap-3">
              <Button variant="ghost" size="icon" onClick={() => navigate(-1)} className="text-white hover:bg-white/10">
                <ArrowLeft className="h-5 w-5" />
              </Button>
              <div className="flex items-center gap-2">
                <div className="h-10 w-10 rounded-xl bg-white/20 flex items-center justify-center">
                  <Shield className="h-5 w-5" />
                </div>
                <div>
                  <h1 className="font-bold text-lg">Painel Admin</h1>
                  <p className="text-xs text-white/70">Gerir plataforma</p>
                </div>
              </div>
            </div>
            <Button variant="ghost" size="icon" className="text-white hover:bg-white/10">
              <Settings className="h-5 w-5" />
            </Button>
          </div>
        </div>

        <div className="pb-20">
          {/* Stats Grid */}
          <div className="px-4 py-6">
            <div className="grid grid-cols-2 gap-3">
              <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: 0.1 }}
              >
                <Card className="p-4 bg-gradient-to-br from-blue-500/10 to-blue-600/5 border-blue-500/20">
                  <div className="flex items-center gap-3">
                    <div className="h-12 w-12 rounded-2xl bg-blue-500 flex items-center justify-center shadow-lg shadow-blue-500/25">
                      <Users className="h-6 w-6 text-white" />
                    </div>
                    <div>
                      <p className="text-3xl font-bold">{stats.totalUsers}</p>
                      <p className="text-xs text-muted-foreground font-medium">Total Usuários</p>
                    </div>
                  </div>
                </Card>
              </motion.div>
              
              <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: 0.15 }}
              >
                <Card className="p-4 bg-gradient-to-br from-green-500/10 to-green-600/5 border-green-500/20">
                  <div className="flex items-center gap-3">
                    <div className="h-12 w-12 rounded-2xl bg-green-500 flex items-center justify-center shadow-lg shadow-green-500/25">
                      <UserCheck className="h-6 w-6 text-white" />
                    </div>
                    <div>
                      <p className="text-3xl font-bold">{stats.verifiedUsers}</p>
                      <p className="text-xs text-muted-foreground font-medium">Verificados</p>
                    </div>
                  </div>
                </Card>
              </motion.div>
              
              <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: 0.2 }}
              >
                <Card className="p-4 bg-gradient-to-br from-orange-500/10 to-orange-600/5 border-orange-500/20">
                  <div className="flex items-center gap-3">
                    <div className="h-12 w-12 rounded-2xl bg-orange-500 flex items-center justify-center shadow-lg shadow-orange-500/25">
                      <AlertTriangle className="h-6 w-6 text-white" />
                    </div>
                    <div>
                      <p className="text-3xl font-bold">{stats.pendingReports}</p>
                      <p className="text-xs text-muted-foreground font-medium">Denúncias</p>
                    </div>
                  </div>
                </Card>
              </motion.div>
              
              <motion.div
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: 0.25 }}
              >
                <Card className="p-4 bg-gradient-to-br from-red-500/10 to-red-600/5 border-red-500/20">
                  <div className="flex items-center gap-3">
                    <div className="h-12 w-12 rounded-2xl bg-red-500 flex items-center justify-center shadow-lg shadow-red-500/25">
                      <Ban className="h-6 w-6 text-white" />
                    </div>
                    <div>
                      <p className="text-3xl font-bold">{stats.blockedAccounts}</p>
                      <p className="text-xs text-muted-foreground font-medium">Bloqueados</p>
                    </div>
                  </div>
                </Card>
              </motion.div>
            </div>
          </div>

          {/* Tabs */}
          <Tabs defaultValue="users" className="w-full px-4">
            <TabsList className="w-full grid grid-cols-2 mb-4 h-12 bg-muted/50 p-1 rounded-xl">
              <TabsTrigger value="users" className="gap-2 rounded-lg data-[state=active]:bg-background data-[state=active]:shadow-sm">
                <Users className="h-4 w-4" />
                Usuários
              </TabsTrigger>
              <TabsTrigger value="reports" className="gap-2 rounded-lg data-[state=active]:bg-background data-[state=active]:shadow-sm relative">
                <FileWarning className="h-4 w-4" />
                Denúncias
                {stats.pendingReports > 0 && (
                  <span className="absolute -top-1 -right-1 h-5 w-5 bg-destructive text-destructive-foreground text-xs font-bold rounded-full flex items-center justify-center">
                    {stats.pendingReports}
                  </span>
                )}
              </TabsTrigger>
            </TabsList>

            <TabsContent value="users" className="space-y-3 mt-0">
              {/* Search */}
              <div className="relative">
                <Search className="absolute left-4 top-1/2 -translate-y-1/2 h-5 w-5 text-muted-foreground" />
                <Input
                  placeholder="Pesquisar usuários..."
                  value={searchQuery}
                  onChange={(e) => setSearchQuery(e.target.value)}
                  className="pl-12 h-12 rounded-xl bg-muted/50 border-0"
                />
              </div>

              {/* Users List */}
              <div className="space-y-2">
                {filteredUsers.map((user, index) => (
                  <motion.div
                    key={user.id}
                    initial={{ opacity: 0, x: -20 }}
                    animate={{ opacity: 1, x: 0 }}
                    transition={{ delay: index * 0.02 }}
                  >
                    <Card className="p-4 hover:bg-muted/30 transition-colors">
                      <div className="flex items-center gap-3">
                        <Avatar 
                          className="h-14 w-14 cursor-pointer ring-2 ring-border"
                          onClick={() => navigate(`/profile/${user.id}`)}
                        >
                          <AvatarImage src={user.avatar_url} />
                          <AvatarFallback className="bg-gradient-to-br from-primary/20 to-accent/20 text-lg font-bold">
                            {user.first_name?.[0] || user.username?.[0]}
                          </AvatarFallback>
                        </Avatar>
                        
                        <div className="flex-1 min-w-0">
                          <div className="flex items-center gap-1.5">
                            <span className="font-bold truncate">
                              {user.full_name || user.first_name || user.username}
                            </span>
                            {user.verified && (
                              <VerificationBadge 
                                verified={user.verified} 
                                badgeType={user.badge_type} 
                                className="h-4 w-4" 
                              />
                            )}
                          </div>
                          <p className="text-sm text-muted-foreground">@{user.username}</p>
                        </div>

                        <div className="flex items-center gap-1">
                          <Button
                            variant="ghost"
                            size="icon"
                            className="h-9 w-9 rounded-full"
                            onClick={() => navigate(`/profile/${user.id}`)}
                          >
                            <Eye className="h-4 w-4" />
                          </Button>
                          
                          <Button
                            variant={user.verified ? "outline" : "default"}
                            size="sm"
                            className="h-9 rounded-full text-xs font-semibold"
                            onClick={() => handleVerifyUser(user.id, !user.verified)}
                          >
                            {user.verified ? "Remover" : "Verificar"}
                          </Button>
                          
                          <Button
                            variant="ghost"
                            size="icon"
                            className="h-9 w-9 rounded-full text-orange-500 hover:bg-orange-500/10"
                            onClick={() => handleBlockUser(user.id)}
                          >
                            <Ban className="h-4 w-4" />
                          </Button>
                          
                          <Button
                            variant="ghost"
                            size="icon"
                            className="h-9 w-9 rounded-full text-destructive hover:bg-destructive/10"
                            onClick={() => setDeleteDialog({ open: true, userId: user.id, username: user.username })}
                          >
                            <Trash2 className="h-4 w-4" />
                          </Button>
                        </div>
                      </div>
                    </Card>
                  </motion.div>
                ))}
              </div>
            </TabsContent>

            <TabsContent value="reports" className="space-y-3 mt-0">
              {reports.length === 0 ? (
                <Card className="p-12 text-center">
                  <div className="h-20 w-20 rounded-full bg-green-500/10 flex items-center justify-center mx-auto mb-4">
                    <CheckCircle2 className="h-10 w-10 text-green-500" />
                  </div>
                  <p className="text-xl font-bold mb-2">Tudo limpo!</p>
                  <p className="text-muted-foreground">Nenhuma denúncia pendente</p>
                </Card>
              ) : (
                reports.map((report, index) => (
                  <motion.div
                    key={report.id}
                    initial={{ opacity: 0, y: 10 }}
                    animate={{ opacity: 1, y: 0 }}
                    transition={{ delay: index * 0.05 }}
                  >
                    <Card className="p-4">
                      <div className="flex items-start justify-between gap-4">
                        <div className="flex-1">
                          <div className="flex items-center gap-2 mb-3">
                            <div className="h-8 w-8 rounded-full bg-orange-500/10 flex items-center justify-center">
                              <AlertTriangle className="h-4 w-4 text-orange-500" />
                            </div>
                            <Badge variant="outline" className="rounded-full">{report.content_type}</Badge>
                          </div>
                          <p className="text-sm mb-2 font-medium">{report.reason}</p>
                          <p className="text-xs text-muted-foreground">
                            ID: {report.reported_content_id}
                          </p>
                        </div>
                        
                        <div className="flex gap-2">
                          <Button
                            variant="outline"
                            size="sm"
                            className="rounded-full"
                            onClick={() => handleResolveReport(report.id, "dismissed")}
                          >
                            Ignorar
                          </Button>
                          <Button
                            size="sm"
                            className="rounded-full"
                            onClick={() => handleResolveReport(report.id, "resolved")}
                          >
                            Resolver
                          </Button>
                        </div>
                      </div>
                    </Card>
                  </motion.div>
                ))
              )}
            </TabsContent>
          </Tabs>
        </div>

        {/* Delete Dialog */}
        <AlertDialog open={deleteDialog.open} onOpenChange={(open) => setDeleteDialog({ ...deleteDialog, open })}>
          <AlertDialogContent className="rounded-2xl">
            <AlertDialogHeader>
              <AlertDialogTitle>Excluir usuário</AlertDialogTitle>
              <AlertDialogDescription>
                Tem certeza que deseja excluir @{deleteDialog.username}? Esta ação não pode ser desfeita.
              </AlertDialogDescription>
            </AlertDialogHeader>
            <AlertDialogFooter>
              <AlertDialogCancel className="rounded-full">Cancelar</AlertDialogCancel>
              <AlertDialogAction 
                onClick={() => handleDeleteUser(deleteDialog.userId)}
                className="bg-destructive hover:bg-destructive/90 rounded-full"
              >
                Excluir
              </AlertDialogAction>
            </AlertDialogFooter>
          </AlertDialogContent>
        </AlertDialog>
      </div>
    </ProtectedRoute>
  );
}
